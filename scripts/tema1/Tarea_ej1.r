#Si hubiéramos empezado a contar segundos a partir de las 12 campanadas que marcan el inicio de 2018, ¿a qué hora de qué día de qué año llegaríamos a los 250 millones de segundos? ¡Cuidado con los años bisiestos!

#Partimos de los 250000000 segundos y calculamos cuantos minutos son

250000000 / 60
# 4166667 minutos

#En este caso, la división ha resultado ser exacta.

#Ahora, tomamos los 4166667 minutos y vemos a cuántas horas corresponden

4166667 / 60 # No es exacta
4166667 %/% 60 
# 69444  horas

#Como la división en este caso no ha sido exacta, calculamos el resto, que son los minutos restantes

4166667 %% 60
# 27 minutos


#Tomamos el total de horas obtenido y calculamos a cuántos días equivalen:

69444 %/% 24
# 2893 días

#De nuevo, la división en este caso no ha sido exacta. Por tanto, calculamos el resto de la división, que son las horas restantes

69444%%24 
# 12 horas


#Consideramos finalmente el número de días totales y calculamos a cuántos años equivalen

2893%/%365
# 7 años (de los cuales 2 son bisiestos)

#Como entre 2018 y 2018 + 7 = 2025 hay 2 años bisiestos, al resto de los días tenemos que quitarle los 2 días correspondientes al día extra de cada año bisiesto

(2893%%365)-2 
# 336 días 


# Sumando al 1 de enero de 2018 7 años, 336 días, 12 horas y 27 minutos, 
# obtenemos que pasados 250000000 de segundos, estaríamos en el 2 de diciembre de 2025 a las 12:27






